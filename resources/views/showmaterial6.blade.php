        <br>
        <b>Pemekatan hayati :</b> bahan beracun yang mencemari perairan mengakibatkan terkontaminasinya racun dalam tubuh organisme. Organisme didalam perairan juga memiliki rantai makanan seperti : apabila ganggang terkontaminasi racun maka zooplakton yang memakan gangang juga terkontaminasi racun. 
        <br>
        <br>
        <b>Kemudian zooplankton </b> dimakan oleh ikan kecil, ikan kecil dimakan ikan besar, ikan besar ditangkap manusia dan dimakan manusia. Sehingga, manusia terkontaminasi racun tersebut dan sangat mengganggu kesehatan. 
        <br>
        <br>
        <b>Mengganggu pemandangan :</b> perairan yang tercemar kadang tidak berbau, tetapi berubah warna. Hal ini mengganggu pemandangan karena air sudah tidak asri lagi. 
        <br>
        <br>
        <b>Mempercepat proses kerusakan benda :</b> sebagian air limbah mengandung zat yang dapat diuraikan oleh bakteri anaerob menjadi gas yang dapat merusak seperti H2S. H2S dapat mempercepat perkaratan pada besi. 
        <br>
        <br>
        <b>Penanggulangan Pencemaran Air :</b> pengolahan limbah bertujuan untuk menetralkan air dari bahan – bahan tersuspensi dan terapung, menguraikan bahan organik biodegradable (bahan organik yang dapat terurai oleh aktiviats makhluk hidup), meminimalkan bakteri patogen (bakteri penyebab penyakit), memperhatikan estetika dan lingkungan. 
        <br>
        <br>
        <div class="alert alert-info" role="alert">
            Pengolahan air limbah dapat dilakukan dengan cara : Pembuatan Kolam Stabilisasi, Instalasi Pengolahan Air Limbah (IPAL) dan Pengelolaan Excreta.
        </div>
        <br>
        <b>Pembuatan Kolam Stabilisasi :</b> air limbah diolah secara alami untuk menetralisasi polutan sebelum air limbah dibuang ke sungai. Kolam stabilisasi yang digunakan yaitu kolam anaerobik, kolam fakultatif (pengolahan air limbah yang tercemar bahan organik pekat) dan kolam maturasi (pemusnahan mikroorganisme patogen). 
        <br>
        <br>
        Kolam stabilisasi dapat digunakan oleh semua kalangan karena mudah memilikinya dan harganya murah. 
        <br>
        <br>
        Instalasi Pengolahan Air Limbah (IPAL) : pengolahan ini dilakukan melalui 3 tahap yaitu
        <br>
        <br>
        <ul>
            <ul>
                <li>Primary treatment (pengolahan pertama) yaitu memisahkan zat cair dan zat padat menggunakan filter (saringan) dan bak sedimentasi.</li>
                <br>
                <li>Secondary treatment (pengolahan kedua) yaitu mengkoagulasikan, menghilangkan koloid dan menstabilisasi zat organik dalam limbah. </li>
                <br>
                <li>Tertiary treatment (pengolahan lanjutan) yaitu penghilangan nutrisi atau unsur hara khususnya nitrat dan fosfat, penambahan klor untuk memusnahkan organisme patogen.</li>
            </ul>
        </ul>
        <br>
        <b>Pengelolaan Excreta :</b> excreta terdapat dalam air limbah rumah tangga, mengandung bakteri patogen, jika tidak dikelola terlebih dahulu menimbulkan berbagai penyakit. Pengolahan excreta dapat dilakukan dengan menampung dan mengolahnya pada jamban atau septictank disekitar tempat tinggal, dialirkan ke tempat pengolahan, dapat dilakukan secara kolekif. 
        <br>
        <br>
        Untuk mencegah meresapnya air limbah excreta ke sumur atau resapan air, jamban yang dibuat harus sehat. Syarat jamban harus sehat : tidak mengotori permukaan tanah, tidak mengotori permukaan air tanah, tidak berbau, sederhana, jauh dari serangga (lalat, nyamuk, kecoa), murah dan bisa diterima masyarakat. 
        <br>
        <br>
        <b>Recycle (daur ulang) :</b> untuk sampah yang dapat terurai dan dapat diolah menjadi hal yang bermanfaat, contohnya sebagai kompos yang dipadukan dengan pemeliharaan cacing tanah. Kompos untuk pupuk dan cacing tanah dapat menyuburkan tanah. 
        <br>
        <br>
        <b>Reuse (penggunaan ulang) : </b> sampah yang tidak dapat terurai dan dapat digunakan ulang. Contohnya botol sirup dapat digunakan lagi untuk menyimpan air minum. 
        <br>
        <br>
        <b>Reduce (pengurangan) : </b> pengurangan bahan atau penghematan. Contohnya jika akan berbelanja ke pasar atau supermarket, sebaiknya membawa tas dari rumah untuk megurangi sampah plastik. 
        <br>
        <br>
        <b>Repair (pemeliharaan) :</b> menjaga lingkungan dengan tidak membuang sampah sembarangan terutama di perairan.   
        <br>
        <b>3. Pencemaran Udara</b>
        <br>
        <br>
        <div class="alert alert-info" role="alert">
            Pencemaran udara adalah kondisi udara mengandung senyawa – senyawa kimia atau substansi fisik atau biologi yang berdampak buruk bagi kesehatan : manusia, hewan, tumbuhan, merusak : keindahan alam, kenyamanan, properti. 
        </div>
        <br>
        Udara mengandung oksigen yang penting untuk kehidupan organisme. Di atmosfer bumi, terkandung sekitar 20% oksigen yang dibutuhkan semua organisme. Oksigen berperan dalam pembakaran karbohidrat melalui pernapasan. Pembakaran juga sering dilakukan di lingkungan pembakaran seperti pembakaran sampah, kayu dan sebagainya.  
        <br>
        <br>
        Hasil samping dari pembakaran adalah karbon (CO2 dan CO) yang dibuang ke udara. CO2 sangat penting untuk tumbuhan dalam proses fotosintesis. Namun, semakin banyaknya populasi manusia kebutuhan tempat tinggal pun meningkat membuat lahan yang ditumbuhi pepohonan berkurang serta adanya illegal loging (penebangan liar) yang membuat tumbuhan berkurang. 
        <br>
        <br>
        Pencemaran udara ada 2 yaitu 1). Primer : disebabkan langsung dari sumber pencemar. Contohnya peningkatan CO2 yang disebabkan oleh aktivitas pembakaran manusia. 2). Sekunder : disebabkan oleh reaksi antar substansi pencemar udara primer yang terjadi di atmosfer. Contohnya pembentukan ozon yang terjadi dari reaksi kimia partikel yang mengandung oksigen. 
        <br>
        <br>
        <b>Faktor Penyebab Pencemaran Udara ada 2 yaitu</b>
        <ul>
            <li>Aktivitas Alam : aktivitas alam seperti bencana alam contohnya gunung meletus menimbulkan abu vulkanik yang mencemari udara sekitar dan merugikan organisme dan kebakaran hutan yang menghasilkan karbondioksida dalam jumlah banyak.</li>
            <li>Aktivitas Manusia : pembakaran sampah, asap – asap industri, asap kendaraan, asap rokok, senyawa kima buangan seperti CFC dan sebagainya. Pencemaran udara dapat berdampak pada kesehatan, tumbuhan, efek rumah kaca dan rusaknya lapisan ozon.</li>
        </ul>
        <br>
        Kesehatan : pencemaran udara menurunkan kualitas udara sehingga menimbulkan banyak penyakit seperti infeksi saluran pernapasan (ISPA) yaitu Emfisema. Emfisema adalah gejala kesulitan pengangkutan oksigen karena CO2 di udara lebih banyak dari O2. Sehingga, tubuh akan kekurangan oksigen dan menjadi sesak napas, pusing, berlanjut pada kematian. 
        <br>
        <br>
        Bagi tumbuhan : abu vulkanik dan hujan asam mengandung sulfur yang tidak bagus untuk tumbuhan karena bisa menyebabkan kematian. Efek rumah kaca : peningkatan suhu bumi yang diakibatkan meningkatnya CO2 dan CO di atmosfer. CO2 dan CO di atmosfer membentuk lapisan yang menghalangi panas bumi keluar, sehingga panas bumi tetap berada didalam bumi. 
        <br>
        <br>
        Rusaknya lapisan ozon : CFC adalah senyawa dalam produk pendingin (freezer, AC, dan aerosol). Ketika CFC terurai di atmosfer, ozon terurai dan lapisan ozon berlubang. Lapisan ozon berfungsi sebagai pelindung bumi dari panas dan sinar UV matahari. Apabila lapisan ozon berlubang, pemanasan global meningkat.
        <br>
        <br>
        <b>4. Pencemaran Tanah</b>
        <br>
        <b>Pencemaran tanah adalah</b> keadaan bahan kimia buatan manusia masuk dan mengubah lingkungan alami tanah. Pencemaran tanah terjadi karena kebocoran limbah atau bahan industri, penggunaan pestisida, kecelakaan kendaraan pengangkut minyak, zat kimia atau limbah, air limbah dari penimbunan sampah dan limbah industri. 
        <br>
        <br>
        Faktor penyebab pencemaran tanah ada 3 yaitu : limbah domestik, limbah industri dan limbah pertanian. 
        <br>
        <br>
        <b>Limbah domestik : </b>berasal dari pedagang, tempat usaha, hotel, kantor – kantor pemerintahan dan tempat wisata. Limbah domestik berupa limbah padat dan cair. Limbah cair berupa tinja (feses), deterjen, oli dan cat. Apabila meresap ke tanah dapat merusak kandungan air tanah dan membunuh mikroorganisme tanah. 
        <br>
        <br>
        Limbah padat berupa senyawa anorganik yang tidak dapat diuraikan (tidak terbiodegradasi) mikroorganisme seperti plastik, serat, keramik, kaleng dan bekas bahan bangunan yang dapat mengurangi kesuburan tanah. Limbah padat akan tetap utuh hingga 300 tahun. 
        <br>
        <br>
        <b>Limbah industri : </b> dari sisa produk industri, berupa limbah padat dan cair. Limbah padat merupakan hasil buangan industri berupa padatan, lumpur dan bubur yang berasal dari proses pengolahan. Contohnya sisa pengolahan pabrik gula, pulp, kertas, rayon, plywood, pengawetan ikan, daging, buah dan sebagainya. 
        <br>
        <br>
        Limbah cair merupakan sisa pengolahan industri pelapisan logam dan industri kimia lainnya. Tembaga, timbal, perak, krom, arsen dan boron adalah zat – zat yang dihasilkan dari proses industri pelapisan logam seperti Hg, Zn, Pb dan Cd. 
        <br>
        <br>
        <b>Limbah pertanian :</b> karena pengetahuan petani kurang, menggunakan pupuk sintetik melebihi ketentuan atau caranya tidak tepat. Akibatnya, limbah pertanian berupa sisa pupuk sintetik untuk menyuburkan tanah membuat tanah tercemar. Misalnya, pupuk urea dan pestisida untuk pemberantas hama tanaman. 
        <br>
        <br>
        Penggunaan pupuk yang terus menerus akan merusak struktur tanah. Akibatnya, kesuburan tanah berkurang dan tidak dapat ditanami jenis tanaman tertentu karena hara tanah semakin berkurang. Penggunaan juga mematikan mikroorganisme yang berguna di dalam tanah. Padahal kesuburan tanah tergantung pada jumlah organisme di dalamnya. 
        <br>
        <br>
        <b>Dampak pencemaran tanah : </b> kromium dan herbisida merupakan bahan karsinogenik untuk semua organisme. Timbal berbahaya bagi anak-anak, menyebabkan kerusakan otak dan kerusakan ginjal pada seluruh organisme. Raksa dan siklodiena menyebabkan kerusakan ginjal. PCB dan siklodiena mengakibatkan kerusakan hati ditandai dengan keracunan. 
        <br>
        <br>
        Organofosfat dan karmabat menyebabkan gangguan saraf otot. Pelarut yang mengandung klorin merangsang perubahan hati, ginjal dan penurunan sistem saraf pusat. Gejalanya sakit kepala, pusing, letih, iritasi mata, dan ruam kulit. Pada dosis tinggi, menyebabkan kematian. 
        <br>
        <br>
        Pencemaran tanah juga memberikan dampak terhadap ekosistem. Perubahan kimiawi tanah timbul dari bahan kimia beracun dan berbahaya. Perubahan ini menyebabkan perubahan metabolisme dari mikroorganisme endemik dan Arthropoda yang hidup di dalamnya. Akibatnya, memusnahkan spesies primer rantai makanan, memberi akibat predator atau tingkatan lain. 
        <br>
        <br>
        Penanggulangan Pencemaran Tanah : remidiasi dan bioremidiasi. Remidiasi adalah kegiatan membersihkan permukaan tanah yang tercemar. Ada 2 jenis remidiasi tanah yaitu in situ (on – site) dan ex situ (off – site). 
        <br>
        <br>
        Pembersihan off – site meliputi penggalian tanah yang tercemar kemudian dibawa ke daerah aman dan dibersihkan dari pencemar. Caranya : disimpan di bak atau tanki yang kedap kemudian diolah dengan IPAL. Pembersihan off – site lebih mahal dan rumit. 
        <br>
        <br>
        Bioremidiasi adalah pembersihan pencemaran tanah menggunakan mikroorganisme (jamur, bakteri). Bioremidiasi bertujuan memecah atau mendegradasi zat pencemar menjadi bahan yang kurang beracun atau tidak beracun (CO2 dan H2O). 
        <br>
        <br>
        Mikroorganisme yang berfungsi sebagai bioremediasi adalah jamur vesikular arbuskular mikoriza, berperan langsung maupun tidak langsung dalam remediasi tanah, berperan langsung menyerap unsur logam dari tanah, menstimulir pertumbuhan mikroorganisme bioremediasi lain seperti bakteri tertentu, jamur spesies lain, dan sebagainya.
    
    
    
    
    
    
    
    
    
    
