        <h3 style="color: aliceblue">
        <b>1. Definisi Pencemaran</b>
        <br>
        <br>
        <div class="alert alert-info" role="alert"><b>Pencemaran lingkungan (environmental pollution)</b> adalah segala sesuatu termasuk bahan – bahan fisika dan kimia yang dapat mengganggu ekosistem. Pencemaran lingkungan merupakan salah satu faktor yang menyebabkan menurunnya kualitas lingkungan. </div>
        <br>
        Menurut UU RI No. 23 Tahun 1997 : pencemaran lingkungan adalah masuknya atau dimasukkannya makhluk hidup, zat, energi atau komponen lain kedalam lingkungan oleh manusia sehingga kualitasnya turun yang menyebabkan lingkungan tidak sesuai peruntukannya. 
        <br>
        <br>
        Di lingkungan terdapat faktor biotik dan abiotik, apabila tidak ada keseimbangan antar keduanya maka tidak ada keseimbangan alam. Pencemaran terjadi akibat kegiatan kumpulan manusia (populasi) dan faktor alam seperti gunung meletus yang menimbulkan abu vulkanik.
        Zat yang dapat mencemari lingkungan dan dapat mengganggu kelangsungan hidup makhluk hidup disebut polutan. Polutan dapat berupa zat kimia, debu, suara, radiasi dan panas.
        <br>
        <br>
        <b>Ciri -ciri dari polutan :</b>
        <ul>
            <ul>
                <li>Kadarnya melebihi diatas normal (melebihi ambang batas) </li>
                <li>Berada pada waktu yang tidak tepat  </li>
                <li>Berada pada tempat yang tak semestinya </li>
            </ul>
        </ul>
        Manusia tidak dapat mencegah pencemaran akibat faktor alam, tetapi dapat mencegah pencemaran akibat kegiatannya sendiri seperti limbah rumah tangga, industri, zat – zat kimia berbahaya, tumpahan minyak, asap hasil pembakaran hutan, minyak bumi dan limbah nuklir. 
        <br>
        <br>
        <b>2. Pencemaran Air</b>
        <br>
        <br>
        <div class="alert alert-info" role="alert">Pencemaran air adalah masuknya makhluk hidup, zat, energi atau komponen lain kedalam air yang menyebabkan air tidak berfungsi sesuai peruntukannya. Pencemaran air merupakan kondisi air dalam keadaan normal. Ciri – ciri air tercemar : warna, bau dan rasanya berubah; pH kurang dari 7 atau lebih dari 7 (pH air = 7). </div>
        Air dapat tercemar oleh komponen – komponen anorganik seperti logam berat yang berasal dari industri : tekstil, pelapisan logam, cat/tinta warna, percetakan, bahan agrokimia dan sebagainya. Kualitas air menentukan kehidupan di perairan laut dan sungai, apabila perairan tercemar maka keseimbangan ekosistem didalamnya akan terganggu. 
        <br>
        <br>
        <b>Limbah industri : </b> limbah industri mengandung zat berbahaya. Kegiatan industri menghasilkan produk utama yaitu bahan jadi dan produk yang tidak terpakai yaitu limbah.
        <b>Jenis limbah yang berasal dari hasil industri yaitu : </b>
        <ul>
            <ul>
                <li>Limbah organik yang bau : hasil industri tekstil dan kertas</li>
                <li>Limbah anorganik berupa cairan panas, berbuih, berwarna, mengandung asam belerang dan berbau menyengat : hasil industri baja, emas, cat, pupuk organik, farmasi dan sebagainya.</li>
            </ul>
        </ul>
        Apabila limbah tersebut dibuang ke saluran air, sungai atau laut akan merusak ekosistem didalamnya. Limbah industri berupa logam berat sering dialirkan ke sungai dan mencemari sungai tersebut. Jenis – jenis logam berat tersebut adalah raksa, timbal, kadmium yang apabila terkonsumsi manusia akan sangat berbahaya. 
        <br>
        <br>
        Contoh pencemaran limbah yaitu pencemaran raksa di Teluk Minamata, Jepang. Para nelayan dan penduduk disekitar teluk memakan ikan yang tercemar raksa tersebut sehingga mengalami kerusakan saraf yang dinamakan penyakit Minamata. Korban yang meninggal akibat penyakit Minamata yaitu lebih dari 80 ribu orang. 
        <br>
        <br>
        <b>Limbah Rumah Tangga adalah </b> limbah yang berasal dari kegiatan perumahan, pasar, perkantoran, penginapan puing – puing bahan bangunan, dan besi tua bekas mesin atau kendaraan. Limbah rumah tangga berupa bahan organik, anorganik, bahan berbahaya dan beracun (B3). 
        <br>
        <br>
        <b>Limbah organik adalah </b> limbah yang dapat diuraikan oleh mikroorganisme, contohnya kulit buah sayuran, sisa makanan, kertas, kayu dan daun. Limbah anorganik adalah limbah yang tidak dapat diuraikan oleh mikroorganisme, contohnya kaca, plastik, besi, alumunium, kaleng susu, kaleng cat, minyak wangi dan sebagainya. 
        Kegiatan pertanian menggunakan obat – obat pembasmi hama seperti insektisida dan pupuk yang berlebihan. Pupuk yang berlebihan dapat menyebabkan suburnya ekosistem perairan. Sehingga, terjadi blooming algae atau tumbuh sumbuh suburnya ganggang di permukaan air. 
        <br>
        <br>
        Ganggang di permukaan air menutupi seluruh permukaan sehingga mengurangi sinar matahari yang masuk kedalam perairan tersebut. Akibatnya proses fotosintesis oleh fitoplankton terganggu dan kadar oksigen yang larut dalam air menurun sehingga meruikan organisme didalamnya. 
        <br>
        <br>
        <b>Penurunan kualitas lingkungan : </b> air tercemar, maka kandungan oksigen didalamnya berkurang dan mengganggu ekosistem didalamnya. Apabila ekosistem perairan terganggu, maka menyebabkan kualitas lingkungan menurun. 
        <br>
        <br>
        <b>Gangguan kesehatan : </b> limbah yang tidak dikelola terlebih dahulu sebelum dibuang, kemungkinan mengandung kuman, bakteri dan virus yang menyebabkan penyakit. Air limbah juga digunakan sebagai sarang nyamuk dan lalat yang membawa (vektor) penyakit tertentu. 
