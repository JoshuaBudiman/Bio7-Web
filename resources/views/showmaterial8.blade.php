            <b>2. Kondisi Bumi </b>
            <br>
            <br>
            Para pelaut mengamati hal yang pertama kali mereka lihat di laut adalah puncak kapal. Ini menunjukkan bahwa bumi bentuknya bulat. Begitu pula pada tahun 1522, Magelhaen telah membuktikan bumi bentuknya bulat, dengan mengadakan pelayaran arah lurus kemudian dia berhasil kembali ke tempat awal dia berlayar. 
            <br>
            <br>
            <b>Rotasi Bumi adalah</b> perputaran bumi pada porosnya. Kala Rotasi Bumi adalah waktu yang dibutuhkan oleh bumi untuk sekali berputar, yaitu 23 jam 56 menit. Bumi berotasi dari barat ke timur. Dampak rotasi bumi yaitu gerak semu harian matahari, perbedaan waktu, pembelokan arah angin dan pembelokan arah arus laut. 
            <br>
            <br>
            <b>Revolusi bumi adalah</b> pergerakan bumi mengelilingi matahari. Kala revolusi bumi adalah waktu yang dibutuhkan oleh bumi untuk sekali mengelilingi matahari yaitu 365,25 hari. Dampak dari revolusi bumi yaitu gerak semu tahunan matahari, perbedaan lamanya siang dan malam, dan pergantian musim. 
            <br>
            <br>
            <b>3. Kondisi Bulan </b>
            <br>
            <br>
            <b>Bulan adalah </b> langit yang terdekat dengan bumi sekaligus satelit bumi. Karena merupakan satelit, bulan tidak dapat memancarkan cahaya sendiri melainkan memancarkan cahaya Matahari. Bulan juga berputar dan mengelilingi bumi. 
            <br>
            <br>
            <b>Bulan berbentuk bulat seperti planet</b>, permukaannya berupa dataran kering, tandus, banyak kawah, terdapat pegunungan dan dataran tinggi. Bulan tidak memiliki atmosfer, sehingga sering terjadi perubahan suhu yang sangat drastis, bunyi tidak dapat merambat, tidak ada siklus air, tidak ditemukan makhluk hidup, dan sangat gelap gulita. 
            <br>
            <br>
            Bulan melakukan tiga gerakan sekaligus, yaitu rotasi, revolusi, dan bergerak bersama dengan bumi untuk mengelilingi matahari. Kala rotasi bulan sama dengan kala revolusinya terhadap bumi yaitu 27,3 hari. Dampak dari pergerakan bulan yaitu terjadinya pasang surut air laut, pembagian bulan, fase-fase bulan, gerhana matahari, dan gerhana bulan. 
            <br>
            <br>
            Pasang adalah peristiwa naiknya permukaan air laut, surut adalah peristiwa turunnya permukaan air laut. Pasang surut air laut terjadi akibat pengaruh gravitasi matahari dan gravitasi bulan. Akibat bumi berotasi pada sumbunya, maka daerah yang mengalami pasang surut bergantian sebanyak dua kali. Ada dua jenis pasang air laut, yaitu pasang purnama dan pasang perbani. 
            <br>
            <br>
            <b>Pasang Purnama</b> dipengaruhi oleh gravitasi bulan, terjadi ketika bulan purnama, menjadi maksimum ketika terjadi gerhana matahari. Hal ini karena dipengaruhi oleh gravitasi bulan dan matahari yang mempunyai arah sama. 
            <br>
            <br>
            <b>Pasang Perbani </b> permukaan air laut turun serendah – rendahnya, terjadi pada saat bulan kuartir pertama dan kuartir ketiga, dipengaruhi oleh gravitasi bulan dan matahari yang saling tegak lurus. 
            <br>
            <br>
            <b>Pembagian bulan ada dua yaitu</b> bulan sideris dan bulan sinodis. Bulan Sideris membutuhkan kala revolusi selama 27,3 hari. Bulan Sinodis membutuhkan kala revolusi selama 29,5 hari. Kala revolusi sinodis dapat ditentukan melalui pengamatan dari saat terjadinya bulan baru sampai bulan baru berikutnya. 
            <br>
            <br>
            Satu bulan sinodis digunakan sebagai dasar penanggalan Komariyah (penanggalan Islam). 
            <br>
            <br>
