            <br>
            <br>
            <ul>
                <ul>
                    <li>Bulan baru terjadi ketika posisi bulan berada di antara bumi dan matahari. Selama bulan baru, sisi bulan yang menghadap ke matahari nampak terang dan sisi yang menghadap bumi nampak gelap. </li>
                    <br>
                    <li>Bulan sabit terjadi ketika bagian bulan yang terkena sinar matahari sekitar seperempat, sehingga permukaan bulan yang terlihat di bumi hanya seperempatnya.</li>
                    <br>
                    <li>Bulan separuh terjadi ketika bagian bulan yang terkena sinar matahari sekitar separuhnya, sehingga yang terlihat dari bumi juga separuhnya (kuartir pertama). </li>
                    <br>
                    <li>Bulan cembung terjadi ketika bagian bulan yang terkena sinar matahari tiga perempatnya, yang terlihat dari bumi juga tiga perempatnya. Sehingga, kita dapat melihat bulan cembung.</li>
                    <br>
                    <li>Bulan purnama terjadi ketika semua bagian bulan terkena sinar matahari, begitu juga yang terlihat dari bumi. Sehingga, kita dapat melihat bulan purnama (kuartir kedua).</li>
                </ul>
            </ul>
