        <h3 style="color: aliceblue">
            <b>1. Sistem Tata Surya </b>
            <br>
            <br>
            Tata surya adalah susunan benda-benda lagit yang terdiri atas matahari sebagai pusat tata surya, planet-planet, komet, meteoroid, dan asteroid yang mengelilingi matahari. Planet yang dekat dengan matahari bergerak lebih cepat daripada planet yang jauh dari Matahari.
            <br>
            <br>
            Bidang edar planet-planet dalam mengelilingi matahari disebut bidang edar dan bidang edar bumi dalam mengelilingi matahari disebut bidang ekliptika. Susunan Tata Surya terdiri atas Matahari, Planet Dalam, Planet Luar, Komet, Meteorid, dan Asteroid.
            <br>
            <br>
            Matahari adalah bintang yang berupa bola gas panas dan bercahaya yang menjadi pusat sistem tata surya. Matahari memiliki empat lapisan yaitu inti matahari, fotosfer, kromosfer, dan korona. 
            <br>
            <br>
            <b>Inti Matahari :</b> memiliki suhu 1,5 x 107°C yang cukup untuk mempertahankan fusi termonuklir dan berfungsi sebagai sumber energi matahari. Energi dari inti diradiasikan ke lapisan luar matahari kemudian sampai ke ruang angkasa.
            <br>
            <br>
            <b>Fotosfer : </b> suhu 6.000 Kelvin, ketebalan 300 km. Energi matahari radiasikan ke luar dan terdeteksi sebagai sinar matahari di bumi. Di dalam fotosfer terdapat bintik matahari yaitu daerah dengan medan magnet yang kuat, dingin dan lebih gelap dari wilayah sekitarnya. 
            <br>
            <br>
            <b>Kromosfer : </b> suhu 4.500 Kelvin, ketebalan 2.000 km. Kromosfer terlihat seperti gelang merah yang mengeliling bulan pada ketika terjadi gerhana matahari total. 
            <br>
            <br>
            <b>Korona : </b> terluar matahari dengan suhu 1.000.000 Kelvin, ketebalan 700.000 km, berwarna keabu – abuan yang dihasilkan dari ionisasi atom karena suhu sangat tinggi, terlihat seperti mahkota dengan warna keabu – abuan yang mengelilingi bulan ketika terjadi gerhana matahari total.
            <br>
            <br>
            <b>Planet adalah </b> benda langit yang tidak dapat memancarkan cahaya sendiri namun bisa memantulkan cahaya dari bintang yang diterimanya. Planet dalam (planet terestrial) adalah planet yang orbitnya dekat dengan Matahari.
            <br>
            <br>
            <b>Planet dalam berukuran kecil</b>, memiliki sedikit satelit, berbatu, terestrial, terdiri atas mineral tahan api seperti silikat yang membentuk kerak dan mantelnya, logam seperti besi dan nikel yang membentuk intinya. Planet dalam terdiri atas Merkurius, Venus, Bumi, dan Mars.
            <br>
            <br>
            <b>Planet luar (planet Jovian) adalah </b> yang orbitnya jauh dari matahari, berukuran besar, memiliki banyak satelit, tersusun dari bahan ringan seperti hidrogen, helium, metana, dan amonia. Planet-planet dalam dan luar dipisahkan oleh sabuk asteroid. 
            <br>
            <br>
            Komet berasal dari Bahasa Yunani yaitu Kometes artinya berambut panjang. Komet adalah benda langit yang mengelilingi matahari dengan orbit sangat lonjong. Komet ini terdiri atas debu, partikel batu yang bercampur dengan es, metana, dan amonia. Berikut gambar komet : 
            <b>Bagian – bagian komet ada 3 yaitu :</b>
            <br>
            <ul>
                <ul>
                    <li>Inti komet : bagian komet yang kecil, padat, tersusun dari debu dan gas </li>
                    <li>Koma : daerah kabut disekitar inti </li>
                    <li>Ekor komet : bagian komet yang panjang, arah ekor komet selalu menjauhi matahari karena dorongan dari angin dan radiasi matahari </li>
                    
                </ul>
