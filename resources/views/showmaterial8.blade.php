            <b>Meteoroid adalah </b> potongan batu atau puing – puing logam yang bergerak di luar angkasa. Meteoroid mengelilingi matahari dengan orbit tertentu dan kecepatan tertentu. Meteoroid tercepat bergerak sekitar 42 km/detik.  Ketika Meteoroid tertarik oleh gravitasi bumi, maka sebelum sampai dibumi, meteorid akan bergesekan dengan atmosfer bumi. 
            <br>
            <br>
            Gesekan tersebut akan menghasilkan panas dan membakar meteoroid tersebut. Meteor adalah meteoroid yang habis terbakar oleh atmosfer bumi. Meteorit adalah meteoroid yang jatuh ke bumi. 
            <br>
            <br>
