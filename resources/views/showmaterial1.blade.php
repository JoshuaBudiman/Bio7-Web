        <br><b style="color: #FF9900">2. Komponen Abiotik</b>
        <br>
        Komponen abiotik terdiri dari faktor fisik maupun kimia yang ada di lingkungan. Contoh dari komponen abiotik daratan ialah hujan, angin, suhu, tanah, ketinggian, dan cahaya matahari. 
        
